num = int (input('Digite um número e saiba do seu sucessor e antecessor: '))

suc = num + 1                   # \n  significa nova linha ou pula uma linha
ant = num - 1

print(f'O sucessor de: {num} é\n {suc}')
print(f'O antecessor de: {num} é\n {ant}')
